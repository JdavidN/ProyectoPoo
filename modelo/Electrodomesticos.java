package modelo;

public class Electrodomesticos 
{
    private String televisor;
    private String nevera;
    private String lavadora;
    private String microondas;
    private String aspiradora;

    public String getAspiradora() 
    {
        return aspiradora;
    }

    public String getLavadora() 
    {
        return lavadora;
    }

    public String getMicroondas() 
    {
        return microondas;
    }

    public String getNevera() 
    {
        return nevera;
    }

    public String getTelevisor() 
    {
        return televisor;
    }

    public void setAspiradora(String aspiradora) 
    {
        this.aspiradora = aspiradora;
    }

    public void setLavadora(String lavadora) 
    {
        this.lavadora = lavadora;
    }

    public void setMicroondas(String microondas) 
    {
        this.microondas = microondas;
    }

    public void setNevera(String nevera) 
    {
        this.nevera = nevera;
    }

    public void setTelevisor(String televisor) 
    {
        this.televisor = televisor;
    }
    
    


}
